    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        model=(DefaultTableModel)jTable1.getModel();
        rowIndex=jTable1.getSelectedRow();
        jRadioButton2_female.setSelected(true);
        jRadioButton1_male.setSelected(false);
        if(model.getValueAt(rowIndex, 3).toString().equals("male"))
        {
        jRadioButton1_male.setSelected(true);
        
        }else
        {
        jRadioButton2_female.setSelected(true);
        
        }
        jTextField1_id.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField1_fname.setText( model.getValueAt(rowIndex, 1).toString());
        jTextField2_lname.setText( model.getValueAt(rowIndex, 2).toString());
        jTextField5_phone.setText( model.getValueAt(rowIndex, 5).toString());
        jTextArea1_address.setText( model.getValueAt(rowIndex, 6).toString());
        
        Date bdate;
        try {
            bdate= new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 4).toString());
            jDateChooser1_birthdate.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(manageStudentsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

