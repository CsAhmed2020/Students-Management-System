     public boolean verifText(){
        if(jTextField1_id.getText().equals("")||jTextField1_fname.getText().equals("")||jTextField2_lname.getText().equals("")||jTextArea1_address.getText().equals("")
                ||jTextField5_phone.getText().equals("")||jDateChooser1_birthdate.getDate()==null)
        {
            JOptionPane.showMessageDialog(null,"One or more empty fields");
        return false;
        //choose a date heigher than current date;
        } else if(jDateChooser1_birthdate.getDate().compareTo(new Date())>0){
            JOptionPane.showMessageDialog(null,"No Student from the future is allowed");
        return false;
        }else
        return true;
    }
